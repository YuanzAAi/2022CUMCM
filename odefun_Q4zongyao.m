function dJ=odefun_Q4zongyao(t,J,Bx)%旋转阻尼器的阻尼系数作为参数键入
I1=5682.908024;%浮子的纵摇转动惯量
I2=304.125;%振子的纵摇转动惯量
K=250000;%扭转弹簧刚度
u2=7142.493;%纵摇附加转动惯量
lamda=1655.909;%纵摇兴波阻尼系数
C=8890.7;%纵摇静水恢复力矩系数
L=2140;%纵摇激励力振幅
w=1.9806;%入射波浪频率
%定义J（1）=θ（1），J（2）=θ（1）'，J（3）=θ（2），J（4）=θ（2）'
dJ=zeros(4,1);
dJ(1)=J(2);
dJ(2)=(L*cos(w*t)-K*(J(1)-J(3))-C*J(1)-Bx*(J(2)-J(4))-lamda*J(2))/(I1+u2);
dJ(3)=J(4);
dJ(4)=(-(Bx*(J(4)-J(2)))-(K*(J(3)-J(1))))/I2;
end 